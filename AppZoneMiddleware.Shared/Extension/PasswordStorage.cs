        public string CryptoHash(string data)
        {
            var noOfRounds = 64;
            byte[] hash = Encoding.UTF8.GetBytes(data);
            //Implement SHA512 64rounds base64 output
            using (SHA512 shaM = new SHA512Managed())
            {

                for (var round = 0; round < noOfRounds; round++)
                {
                    hash = shaM.ComputeHash(hash);
                }

            }

            return Convert.ToBase64String(hash);
        }
    }

    class InvalidHashException : Exception
    {
        public InvalidHashException() { }
        public InvalidHashException(string message)
            : base(message) { }
        public InvalidHashException(string message, Exception inner)
            : base(message, inner) { }
    }

    class CannotPerformOperationException : Exception
    {
        public CannotPerformOperationException() { }
        public CannotPerformOperationException(string message)
            : base(message) { }
        public CannotPerformOperationException(string message, Exception inner)
            : base(message, inner) { }
    }
}
